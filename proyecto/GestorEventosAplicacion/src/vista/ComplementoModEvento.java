    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            getE().setCodigo(jTextField9.getText());
            getE().setNombre(jTextField1.getText());
            getE().setLugar(jTextField2.getText());
            int i = jComboBox1.getSelectedIndex();
            getE().setProveedor(jComboBox1.getItemAt(i));
            i = jComboBox2.getSelectedIndex();
            getE().setTipo(jComboBox2.getItemAt(i));
            getE().setfInicio(jTextField3.getText());
            getE().setfFin(jTextField5.getText());
            getE().setfLimite(jTextField7.getText());
            getE().setCostoTotal(jTextField4.getText());
            getE().setCantParticipantes(jTextField6.getText());
            getE().setPorcSubsidio(jTextField8.getText());
            if(jTextField10.getText().equals(""))
                getE().setCopago();
            else
                getE().setCopago(Double.parseDouble(jTextField10.getText()));
            getE().setObservacion(jTextArea1.getText());
            String s = getPrevious().getMenu().getControlF().ModificarEvento(getE());
            JOptionPane.showMessageDialog(null, s);
            jButton2ActionPerformed(evt);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

