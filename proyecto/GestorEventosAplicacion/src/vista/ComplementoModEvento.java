    public void fillList() throws Exception {
        ArrayList<String> l = new ArrayList<String>();
        l = getPrevious().getMenu().getControlM().consultarProveedores();
        for (String s : l) {
            jComboBox1.addItem(s);
        }
        l = getPrevious().getMenu().getControlM().consultarTiposEventos();
        for (String s : l) {
            jComboBox2.addItem(s);
        }
    }

