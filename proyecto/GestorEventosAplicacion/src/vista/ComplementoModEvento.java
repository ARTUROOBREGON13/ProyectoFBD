    private void cargar() throws Exception {
        e = getPrevious().getEvento();
        jTextField9.setText(e.getCodigo());
        jTextField9.disable();
        jTextField1.setText(e.getNombre());
        jTextField1.disable();
        jTextField2.setText(e.getLugar());
        jTextField2.disable();
        jComboBox1.setSelectedItem(e.getProveedor().getNit() + " - " + e.getProveedor().getNombre());
        jComboBox1.disable();
        jComboBox2.setSelectedItem(e.getTipo());
        jComboBox2.disable();
        jTextField3.setText(e.getfInicio());
        jTextField3.disable();
        jTextField5.setText(e.getfFin());
        jTextField5.disable();
        jTextField7.setText(e.getfLimite());
        jTextField4.setText(String.valueOf(e.getCostoTotal()));
        jTextField4.disable();
        jTextField6.setText("" + e.getCantParticipantes());
        jTextField8.setText("" + e.getPorcSubsidio());
        jTextField10.setText(String.valueOf(e.getCopago()));
        jTextArea1.setText(e.getObservacion());
        fillList();
    }

